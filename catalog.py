def search_catalog(query: str, test_types: list = None, max_results: int = 10) -> list:
    """Simple keyword-based search over the catalog."""
    query_lower = query.lower()
    scored = []

    for item in SHL_CATALOG:
        score = 0

        # Check name match
        if any(word in item["name"].lower() for word in query_lower.split()):
            score += 3

        # Check keyword match
        for kw in item["keywords"]:
            if kw in query_lower:
                score += 2

        # Check description match
        desc_words = item["description"].lower()
        for word in query_lower.split():
            if len(word) > 3 and word in desc_words:
                score += 1

        # Filter by test type if specified
        if test_types and item["test_type"] not in test_types:
            continue

        if score > 0:
            scored.append((score, item))

    # Sort by score descending
    scored.sort(key=lambda x: x[0], reverse=True)
    return [item for _, item in scored[:max_results]]


def get_assessment_by_name(name: str) -> dict:
    """Find an assessment by name (partial match)."""
    name_lower = name.lower()
    for item in SHL_CATALOG:
        if name_lower in item["name"].lower():
            return item
    return None
